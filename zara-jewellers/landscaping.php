                    <p class="text-justify">
                       The corporate nature of the modern university is adversely affecting the learning experience for students of landscape architecture. Programs are susceptible to the volatility of the market and the size of classes are undermining the quality of the education. These were some of the sentiments heard at a recent discussion in Melbourne which aimed to explore some of the key issues currently facing landscape architecture education, both within Victoria and more broadly. The event, organized by AILA Cultivate, a sub-committee of the Victorian chapter of AILA, follows a round-table conversation held at the University of Melbourne in 2018 on “The Future Practitioner,” and was catalyzed by appointments of new leadership at two of Victoria’s three landscape architecture programs. The discussion, which took place in mid November, also precedes an imminent AILA discussion paper on the topic that is aiming to summarize issues and opportunities and make recommendations regarding strategic priorities and resourcing requirements.<br>

                        Featured guests to the discussion were Beau Beza, associate head of School, Teaching and Learning at the Deakin University’s School of Architecture and Built Environment; Sidh Sintusingha, senior lecturer in Landscape Architecture at the University of Melbourne; and Katrina Simon, associate dean, Landscape Architecture at RMIT University.<br>

                        Three major interlinked themes emerged on the evening in response to questions jointly posted by AILA Victoria’s Cultivate and Education sub-committees, as well as to questions from the audience.<br>


